class I18N:
    """
    Internationalization engine.
    Translates keys into localized strings based on the selected language.
    """
    def __init__(self, language: str = "en"):
        self.language = language

    def translate(self, key: str, **kwargs) -> str:
        """
        Translates a key into the active language.
        
        Args:
            key: The translation key.
            kwargs: Values to be formatted into the string.
            
        Returns:
            The translated and formatted string.
        """
        lang = self.language if self.language in TRANSLATIONS else "en"
        text = TRANSLATIONS[lang].get(key, key)
        if kwargs:
            try:
                return text.format(**kwargs)
            except (KeyError, ValueError):
                return text
        return text
